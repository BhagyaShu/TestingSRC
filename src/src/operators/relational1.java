package operators;

public class relational1 {

	
	public static void main(String[] args) {
		
		int a = 30, b = 35 ;
		
		System.out.println(a>b);
		
		System.out.println(b<a);
		
		System.out.println(b==a);
		
		System.out.println(a!=b);
		
		System.out.println(a<=b);
		
		System.out.println(b<=a);
		
		System.out.println("Value of a ="+ a + "Value of b ="+ b);
		
		
				
	}
}

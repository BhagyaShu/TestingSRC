package operators;

public class relational {
	
	public static void main(String[] args) {
		
		int a = 30, b = 30;
		
		System.out.println("Value of a is "+ a   + "Value of b is " + b);
		
		System.out.println(a<b);
		
		System.out.println(a>b);
		
		System.out.println(a<=b);
		
		System.out.println(a>=b);
		
		System.out.println(a==b);
		
		System.out.println(a!=b);
		 
		
		
		
}
	

}
